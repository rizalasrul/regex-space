        </p>
        <h2>Adaptasi dengan budaya startup</h2>
        <p>
          Setelah memastikan <i>hardskill</i> kita terpenuhi, maka selanjutnya adalah memastikan bahwa kita dapat memahami budaya tempat kita bekerja dan memahami apa yang diharapkan oleh startup dari karyawan mereka. Tentu saja budaya setiap startup itu beragam. Tetapi sepengamatan saya, budaya yang pasti ada adalah:
        </p>
        <h3>1. Budaya untuk selalu bereksperimen dan berinovasi</h3>
        <p>
          Seperti yang sudah saya tulis di atas, bahwa startup itu sudah pasti akan selalu berinovasi. Selain itu yang tidak kalah penting adalah budaya bereksperimen. Biasanya startup memberikan kelonggaran atau fleksibilitas terhadap karyawannya untuk melakukan eksperimen-eksperimen terkait bidang pekerjaan mereka. Kita cukup mendapatkan arahan <i>high level</i> tanpa harus diberikan list mulai A ke Z dari apa yang harus dikerjakan.
        </p>
        <p>
          Hal ini berarti kita dituntut untuk dewasa dalam bertindak. Meskipun tidak terjadi <i>micromanage</i>, sangat penting untuk kita tetap berpegang terhadap KPI (Key Performance Indicator) atau tanggung jawab kita, dan bersikap profesional terhadap hal itu.
        </p>
        <p>
          Di Bukalapak sendiri memiliki core value <b>try, fail, and try again</b>, sehingga Bukalapak berharap talentanya berani untuk bereksperimen dan juga berani menerima ketika menemui kegagalan, serta belajar dari kegagalan tersebut. Core value yang lain adalah <b>go the extra mile</b> juga berlaku. Bukalapak juga berharap talentanya tidak hanya berhenti ketika KPI mereka tercapai. Tetapi terus berinovasi untuk mencapai tanggung jawab yang lebih baik lagi kedepannya.
        </p>
        <h3>2. Budaya untuk bekerja cepat</h3>
        <p>
          Secara umum, semua startup memiliki budaya kerja yang cepat jika dibandingkan dengan <i>corporate company</i>. Alur kerja di tech startup pada umumnya menerapkan metode agile atau semacamnya untuk menghasilkan outcome dengan cepat.
        </p>
        <p>
          Pengalaman pribadi saya, apa yang saya pelajari di Bukalapak (saat itu saya baru tiga bulan bergabung) jauh lebih banyak daripada apa yang saya pelajari di tempat sebelumnya, padahal di tempat sebelumnya, saya bekerja selama sekitar setahun.
        </p>
        <p>
          Lagi-lagi disinilah penting bagi kita untuk selalu mengasah diri. Mungkin saja tanggung jawab baru yang kita emban adalah bidang yang tidak sesuai atau tidak kita kuasai. Atau mungkin saja teknologi yang kita pelajari beberapa tahun sebelumnya sudah tidak relevan lagi untuk beberapa bulan kedepan.
        </p>
