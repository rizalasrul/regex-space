        </p>
        <p>
          Lingkungan itu akan mencerminkan kepribadian developer sama kuatnya seperti halnya hobi, pakaian, hingga potongan rambutnya. Namun, jika kita seorang programmer yang pragmatis, kita akan terlihat memiliki karakteristik seperti berikut ini:
        </p>
        <h3>Early adopter/fast adapter</h3>
        <p>
          Kita memiliki naluri terhadap teknologi dan suka mencoba berbagai hal. Ketika diberi sesuatu yang baru, kita dapat menangkapnya dengan cepat dan mengintegrasikannya dengan seluruh wawasan yang kita punya. <i>Our confidence is born of experience</i>.
        </p>
        <h3>Memiliki keingintahuan yang sangat tinggi</h3>
        <p>
          Kita cenderung bertanya “wow kodemu rapi sekali. bagaimana caramu melakukannya? Apakah kamu memiliki masalah dalam menggunakan library tersebut? Lalu, bagaimana cara implementasinya ya?”. <i>You are a pack rat for little facts, each of which may affect some decision years from now</i>.
        </p>
        <h3>Critical thinker</h3>
        <p>
          Kita jarang menerima pekerjaan tanpa diberikan maksud dari apa yang akan dikerjakan terlebih dahulu. Ketika kita mendapatkan sebuah pekerjaan, lalu atasan kita berkata “pekerjaan ini harus dilakukan karena akan memberikan solusi ke banyak orang”, mulailah kita mencium pekerjaan tersebut sebagai tantangan.
        </p>
        <h3>Realistic</h3>
        <p>
          Kita mencoba memahami <i>root cause</i> dari setiap masalah yang kita hadapi. Karakteristik realistis ini memberi kita perasaan yang baik tentang betapa sulitnya berbagai hal, dan berapa lama hal itu akan terjadi. Memahami bahwa suatu proses harus sulit atau akan memakan waktu cukup lama untuk diselesaikan, memberikan kita kekuatan dan perhitungan yang tepat untuk tetap melakukannya.
        </p>
        <h3>Orang yang serba tahu (generalis)</h3>
        <p>
          Kita berusaha keras untuk terbiasa dengan berbagai teknologi dan lingkungan, dan kita berupaya untuk mengikuti tren teknologi baru. Meskipun pekerjaan kita saat ini mengharuskan untuk menjadi seseorang yang spesialis, kita akan selalu dapat pindah ke bidang dan tantangan baru.
        </p>
        <p>
          Selain keempat karakteristik di atas, ada dua tips dasar yang menjadikan diri kita sebagai programmer pragmatis:
        </p>
        <ol>
          <li>
            <b>Care about your craft</b>
            <p style="margin-top: 0px;">
              Tidak ada gunanya mengembangkan software atau aplikasi, kecuali kita melakukannya dengan kepedulian dan kehati-hatian.
            </p>
          </li>
          <li>
            <b>Think! about your work</b>
            <p style="margin-top: 0px;">
              Alih-alih berpikir untuk menjadi pragmatis programmer, lebih baik berpikir terhadap apa yang kita kerjakan hari ini. Fokus terhadap yang proyek yang kita kerjakan sekarang. <i>Never run on autopilot</i>. Teruslah berpikir, selalu kritis terhadap pekerjaan kita. Secara tidak langsung, kita telah menerapkan mantra programmer pragmatis: THINK.
            </p>
          </li>
        </ol>
        <h2>Individual yang pragmatis di tim yang besar</h2>
        <p>
          Beberapa orang merasa bahwa tidak ada ruang untuk individualitas di tim yang besar atau di proyek yang kompleks. Mereka bilang “<i>software construction is an engineering discipline. Hal itu akan rusak jika salah seorang dari tim tersebut membuat keputusan untuk diri mereka sendiri</i>”.
        </p>
        <p>Hal tersebut sangat tidak benar.</p>
        <p>
          <i>The construction of software should be an engineering discipline</i>. Namun, ini tidak menghalangi keahlian tiap individu. Coba bayangkan sebuah museum yang dibangun pada saat ratusan tahun yang lalu. Pekerjaan pembangunan tersebut dilakukan puluhan, atau mungkin ratusan orang, yang tersebar di beberapa dekade. Lalu pekerjaan tersebut dilanjutkan ke orang yang berbeda tiap dekadenya. Keilmuwan dalam membangun museum tersebut juga berkembang tiap dekadenya, sehingga pekerja yang melanjutkan pembangunan tersebut memiliki keilmuan yang berkembang. Tiap individu dari pekerja tersebut dapat berkontribusi dengan kemampuan individu yang dia miliki, tanpa harus mengesampingkan kemampuannya sebagai bagian dari tim yang membangun museum tersebut.
        </p>
        <h2>Poinnya adalah proses yang berkelanjutan</h2>
        <blockquote>
          <p>
